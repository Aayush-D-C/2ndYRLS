# ---------------------------------Operators----------------------------------------------------
"""
This script covers the basic operators in Python:
1. Arithmetic Operators
2. Relational (Comparison) Operators
3. Logical Operators
"""

# Arithmetic Operators -> + , - , * , /
num1 = 10
num2 = 30

print("Addition:", num1 + num2)
print("Subtraction:", num1 - num2)
print("Division:", num1 / num2)
print("Multiplication:", num1 * num2)

# Relational Operators -> <,>,==,!=
print("Is num1 less than num2?", num1 < num2)
print("Is num1 greater than num2?", num1 > num2)
print("Is num1 equal to num2?", num1 == num2)
print("Is num1 not equal to num2?", num1 != num2)

# Logical Operators -> &, |
log1 = True
log2 = False

print("Logical AND:", log1 & log2)  # AND
print("Logical OR:", log1 | log2)   # OR
print("Logical NOT on log1:", not log1)  # NOT
